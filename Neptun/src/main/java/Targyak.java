public class Targyak {
    private String nev;
    private String oktato;

    public Targyak(String nev, String oktato) {
        this.nev = nev;
        this.oktato = oktato;
    }

    public String getNev() {
        return nev;
    }

    public void setNev(String nev) {
        this.nev = nev;
    }

    public String getOktato() {
        return oktato;
    }

    public void setOktato(String oktato) {
        this.oktato = oktato;
    }
}
